---//成功率roll----------------------------------------------------------------------------------------
function ZPetRifineThrowDice(chance)
	if type(chance)~="number" or chance<=0 then 
		chance=0
	end 
	if chance>100 then chance=100 end 
	local zrollresult=0
	local zrolldice= math.random()*100   
	if zrolldice<chance and zrolldice~=0 then 
		zrollresult=1
	else 
		zrollresult=0
	end 
	return zrollresult
end 
